package huynhph30022.fpoly.assignment.model;

public class ThanhVien {
    private int maThanhVien;
    private String namSinhThanhVien;
    private String hoTenThanhVien;

    public ThanhVien() {
    }

    public int getMaThanhVien() {
        return maThanhVien;
    }

    public void setMaThanhVien(int maThanhVien) {
        this.maThanhVien = maThanhVien;
    }

    public String getNamSinhThanhVien() {
        return namSinhThanhVien;
    }

    public void setNamSinhThanhVien(String namSinhThanhVien) {
        this.namSinhThanhVien = namSinhThanhVien;
    }

    public String getHoTenThanhVien() {
        return hoTenThanhVien;
    }

    public void setHoTenThanhVien(String hoTenThanhVien) {
        this.hoTenThanhVien = hoTenThanhVien;
    }
}
